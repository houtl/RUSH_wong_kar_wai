#include "game_2048.h"

int full_map(t_para *p)
{
	int x;
	int y;

	y = -1;
	while (++y < p->size)
	{
		x = -1;
		while (++x < p->size)
			if (p->map[y][x] == 0)
				return (0);
	}
	return (1);
}
