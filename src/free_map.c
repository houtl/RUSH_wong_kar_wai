#include "game_2048.h"

void	free_map(t_para *p)
{
	int	i;
	int j;
	int	**map;

	i = -1;
	j = -1;
	map = p->map;
	while (++i < p->size)
	{
		while (++j < p->size)
			free(map[i]);
	}
}
