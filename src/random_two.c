#include "game_2048.h"

void	random_two(t_para *p)
{
	p->x = (int)rand() % p->size;
	p->y = (int)rand() % p->size;
	p->z = ((int)rand() % 10) ? 2 : 4;
	if (p->map[p->x][p->y] == 0)
	{
		p->map[p->x][p->y] = p->z;
		return ;
	}
	else
		random_two(p);
}
